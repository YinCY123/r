# R has five basic or 'atomic' classes of objects:
# storage mode

1. character
2. numeric (real numbers)
3. interger
4. complex
5. logical (TRUE/FALSE)


# Missing Values
Missing values are denoted by `NA` or `NaN` for undefined mathematical operations.

1. `NA` values have a class also, so there are integer `NA`, character `NA`, etc.

2. A `NaN` value is also `NA` but the converse is not true.
